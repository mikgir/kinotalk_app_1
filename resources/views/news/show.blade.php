                                <p>22 декабря на большие экраны выходит новый российский фантастический фильм «Мира» режиссёра Дмитрия Киселёва,
                                    часть съёмок которого проходила во Владивостоке. Более того – город стал одной из центральных точек сюжета,
                                    поскольку он разворачивается здесь и в космосе. Предпремьерный показ для партнёров, тех,
                                    кто участвовал и помогал в съёмках во Владивостоке, провели 16 декабря в кинотеатре «Океан».
                                </p>
                                <p>Напомним, часть съёмок фильма проходила в дальневосточной столице осенью 2021 года при поддержке
                                    министерства культуры и архивного дела Приморского края и администрации города. Для этого среди горожан набирали массовку и
                                    даже искали дублёра актрисе Дарье Мороз. Из локаций были задействованы Спортивная набережная, набережная Цесаревича,
                                    Токаревский маяк и недостроенная 25-этажная башня ЖК «Аркада АРТ», зрители увидят и кампус ДВФУ, снаружи и изнутри.
                                </p>
                                <p>Город, который занимает центральное место в сюжете, увидел премьеру «первым во Вселенной» – в
                                    Москве показ состоялся также 16 декабря, но вечером, когда во Владивостоке уже наступило «завтра».
                                </p>
                                <p>Сюжет фильма разворачивается в недалёком будущем. Лера Арабова, 15-летняя девочка, живущая с семьёй –
                                    мамой, её новым мужем и восьмилетним братом – во Владивостоке. Отец Леры уже много лет работает на орбитальной космической станции «Мира».
                                    Их с дочерью отношения разладились давно, она даже называет его не «папой», а просто «Арабов», и в этом большая трагедия для обоих.
                                </p>
                                <p>В фильме снимались Татьяна Догилева, Анатолий Белый, Евгений Егоров, Дарья Мороз,
                                    Максим Лагашкин, Андрей Смоляков, Антон Богданов. Бюджет фильма при поддержке Фонда кино составил 540 млн рублей,
                                    что по современным меркам не так уж и дорого (7-8 млн долларов), особенно для кинокартины, состоящей из спецэффектов.
                                </p>
                                <p>Визуально фильм получился очень богатым и достойным. Как уже говорилось, зрители увидят здесь многие знаковые точки города.
                                    Золотой мост, конечно, будет разрушен – и в этом нет ничего удивительного, поскольку в фильмах-катастрофах довольно часто
                                    страдают главные достопримечательности города, в котором эта катастрофа происходит. С точки зрения самих съёмок, спецэффектов и массовки,
                                    пожалуй, «Мира» – один из лучших российских фильмов последнего десятилетия. Звуковые эффекты и саундтрек тоже радуют. Главная песня фильма –
                                    старый хит «Туда» Михея и Джуманджи.
                                </p>
                                <p>«В «Мире» нам удалось сделать многое – например, снять 8-минутную однокадровую сцену падения метеоритов, подобно которой по
                                    постановке в российском кино ещё не было; сделать одним из героев фильма сам город Владивосток, который,
                                    будучи крайне кинематографичным местом, в нашем кино очень мало представлен. Мы снимали в недрах самого настоящего танкера и в декорации,
                                    объятой огнём; собирали сотни актёров массовых сцен, десятки машин спецтехники и прорабатывали космическую станцию до самого последнего винтика.
                                    Вообще, декорации в «Мире» – отдельный повод для гордости», – делится автор идеи и продюсер фильма Нарек Мартиросян.
                                </p>
                                <p>«Мира» – это некоторым образом научная фантастика. Она имеет право быть, ведь астероиды и исходящая от них опасность существуют на самом деле,
                                    учёные нам показывают, насколько близко они могут пролетать от Земли и как могут менять свою траекторию. Проблема попадания больших астероидов
                                    на нашу планету реально существует. Как пример – огромное количество озёр образовались именно после метеоритного удара. Научная фантастика
                                    рисует недалёкое будущее, 20-30 лет вперёд, когда мы уже будем летать на станциях типа «Миры». Уже сегодня человечеству по силам создать
                                    такие аппараты, равно как и искусственный интеллект – мы уже сейчас в рамках эксперимента используем его элементы. Так же как уже сейчас на МКС
                                    мы в качестве эксперимента используем 3D-принтеры. Думаю, пройдёт не так много времени, и мы начнём использовать их как штатное оборудование на
                                    каждой станции», – считает Юрчихин.
                                </p>
                                <p>Большинство посмотревших предпремьерный показ остались довольны и сюжетом, и тем, как показан в картине Владивосток,
                                    который невозможно не узнать. Люди отмечали и захватывающие сцены, и видовые кадры, и работу актёров.
                                    Некоторым же зрителям фильм пришёлся не по нраву именно своим несовпадением с тем, как это должно выглядеть в реальности.
                                    Упоминали и то, что в американских фильмах, например, герои замахиваются на спасение сразу всего мира, а не отдельно взятых персон.
                                    Не обошлось и без параллелей с фильмом 1998 года «Армагеддон». Последний, кстати, очень ругали критики, астронавты, физики и вообще все кому не
                                    лень, зато зрителям он полюбился.
                                </p>
